    def __init__(self, model, collection, alpha: float = 0.25, rrf_k: int = 60):
        self.model = model
        self.collection = collection
        self.alpha = alpha
        self.rrf_k = rrf_k

    # ------------------------------------------------------------------ #
    #  公开接口                                                             #
    # ------------------------------------------------------------------ #

    def invoke(self, query: str, k: int = 3):
        """返回 top-k 文档列表，每个元素有 .page_content 和 .metadata 属性。"""
        # 候选池大小：至少取 max(30, k*10) 条供两路各自排名
        pool = min(max(k * 10, 30), self.collection.count())

        # --- 自适应 alpha：根据查询的关键词信号强度动态调整 ---
        alpha = self._adaptive_alpha(query)

        # --- 向量路：余弦相似度排名 ---
        vec_ranking = self._vector_rank(query, pool)

        # --- 关键词路：关键词命中分排名 ---
        kw_ranking = self._keyword_rank(query, vec_ranking)

        # --- RRF 融合（传入自适应 alpha）---
        fused = self._rrf_fuse(vec_ranking, kw_ranking, alpha)

        # 政策文件软惩罚：将"未知"类型文档排到候选末尾
        # 政策文件在语义上可能与具体诈骗类型相似（如都提到借贷、转账），
        # 但对用户查询的实际帮助远低于具体案例，因此降低其排名优先级。
        policy_docs = [(doc, meta) for doc, meta in fused
                       if meta.get('fraud_type', '') in ('未知', '', None)]
        case_docs   = [(doc, meta) for doc, meta in fused
                       if meta.get('fraud_type', '') not in ('未知', '', None)]
        fused = case_docs + policy_docs

        # 返回 top-k
        results = []
        for doc_text, metadata in fused[:k]:
            class _Doc:
                def __init__(self, content, meta):
                    self.page_content = content
                    self.metadata = meta
            results.append(_Doc(doc_text, metadata))
        return results

    def _adaptive_alpha(self, query: str) -> float:
        """
        根据查询的关键词信号强度动态调整 alpha。

        判断逻辑基于两个独立信号：
          - TYPE_RULES 命中：说明查询含有指向特定诈骗类型的触发词
          - FRAUD_KEYWORDS 高权重词（weight>=5）命中数

        四档映射（v3 改进版，解决了旧版91%查询堆积在0.25的问题）：
          TYPE_RULES命中 且 高权重词>=2 → alpha=0.40  （双强信号，关键词路最可信）
          仅 TYPE_RULES命中             → alpha=0.20  （类型确定但词汇较口语化）
          仅 高权重词>=1（无TYPE_RULES）→ alpha=0.15  （有领域词但类型不明确）
          两者均无                      → alpha=0.05  （口语化查询，依赖向量路）

        设计依据：
          - 开发集（结构化查询）：约29%双强信号→0.40，66%仅TYPE_RULES→0.20
            接近 tune_alpha 在开发集找到的最优值 0.5，比旧版0.25更高效
          - 保留集（口语化查询）：约28%双强信号→0.40，63%仅TYPE_RULES→0.20
            主体从旧版的0.25降低到0.20，给向量路更多权重，改善口语查询召回
        """
        # 高权重词命中（weight >= 5 的领域专属词）
        high_kws = [k for k, w in self.FRAUD_KEYWORDS.items()
                    if w >= 5 and k in query]
        # TYPE_RULES 是否有任意规则触发
        type_triggered = any(
            any(t in query for t in triggers)
            for _, triggers in self.TYPE_RULES
        )

        if type_triggered and len(high_kws) >= 2:
            return 0.40   # 双强信号：关键词路高度可信
        elif type_triggered:
            return 0.20   # 类型明确但词汇口语化：向量路为主
        elif len(high_kws) >= 1:
            return 0.15   # 有领域词但类型不明：轻度关键词辅助
        else:
            return 0.05   # 纯口语化：几乎完全依赖向量路

    # ------------------------------------------------------------------ #
    #  向量路                                                               #
    # ------------------------------------------------------------------ #

    def _vector_rank(self, query: str, pool: int) -> list[tuple[str, dict]]:
        """用余弦相似度检索，返回按相似度降序排列的 (doc_text, metadata) 列表。"""
        qe = self.model.encode(query)
        res = self.collection.query(
            query_embeddings=[qe.tolist()],
            n_results=pool,
        )
        if not res['documents'] or not res['documents'][0]:
            return []
        return list(zip(res['documents'][0], res['metadatas'][0]))

    # ------------------------------------------------------------------ #
    #  关键词路                                                             #
    # ------------------------------------------------------------------ #

    def _keyword_score(self, doc_text: str, query: str) -> float:
        """计算单个文档的关键词得分（只在候选池内排名用，不与向量分直接相加）。"""
        score = 0.0

        # 1. 关键词命中
        for kw, w in self.FRAUD_KEYWORDS.items():
            if kw in query and kw in doc_text:
                score += w

        # 2. 类型规则加分
        for type_token, query_triggers in self.TYPE_RULES:
            if type_token in doc_text and any(t in query for t in query_triggers):
                score += 10.0

        # 3. 查询词直接命中（兜底）
        for token in re.findall(r'[\u4e00-\u9fa5]{2,}', query):
            if token in doc_text:
                score += 1.0

        return score

    def _keyword_rank(self, query: str,
                      candidates: list[tuple[str, dict]]) -> list[tuple[str, dict]]:
        """对候选池按关键词得分重新排序，返回降序列表。"""
        scored = [(self._keyword_score(doc, query), doc, meta)
                  for doc, meta in candidates]
        scored.sort(key=lambda x: x[0], reverse=True)
        return [(doc, meta) for _, doc, meta in scored]

    # ------------------------------------------------------------------ #
    #  RRF 融合                                                             #
    # ------------------------------------------------------------------ #

    def _rrf_fuse(self,
                  vec_ranking: list[tuple[str, dict]],
                  kw_ranking:  list[tuple[str, dict]],
                  alpha: float = None) -> list[tuple[str, dict]]:
        """
        RRF 公式：score(d) = (1-α)·1/(k+rank_vec) + α·1/(k+rank_kw)

        两路排名用同一候选集，所以每个文档在两路中都有排名。
        alpha=0 → 纯向量；alpha=1 → 纯关键词。
        alpha 参数优先使用传入值，其次回退到 self.alpha（固定模式兼容）。
        """
        k = self.rrf_k
        alpha = alpha if alpha is not None else self.alpha

        # 建立排名查找表（排名从1开始）
        vec_rank  = {doc: i+1 for i, (doc, _) in enumerate(vec_ranking)}
        kw_rank   = {doc: i+1 for i, (doc, _) in enumerate(kw_ranking)}

        # 所有候选文档的并集
        all_docs = {doc: meta for doc, meta in vec_ranking}

        scores = {}
        for doc in all_docs:
            rv = vec_rank.get(doc, len(vec_ranking)+1)
            rk = kw_rank.get(doc,  len(kw_ranking)+1)
            scores[doc] = (1 - alpha) / (k + rv) + alpha / (k + rk)

        sorted_docs = sorted(scores, key=lambda d: scores[d], reverse=True)
        return [(doc, all_docs[doc]) for doc in sorted_docs]


